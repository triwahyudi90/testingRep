package com.aswata.report.entity;

/**
 * @author Tri Wahyudi
 *
 */
public class Bsn {
	private String bsnId;
	private String name;
	private String cob;
	/**
	 * @return the bsnId
	 */
	public String getBsnId() {
		return bsnId;
	}
	/**
	 * @param bsnId the bsnId to set
	 */
	public void setBsnId(String bsnId) {
		this.bsnId = bsnId;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the cob
	 */
	public String getCob() {
		return cob;
	}
	/**
	 * @param cob the cob to set
	 */
	public void setCob(String cob) {
		this.cob = cob;
	}
	
	
}
